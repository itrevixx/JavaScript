/*  Escribe una función echo que devuelva el valor que recibe como parámetro.

echo("Greta");
debería devolver «Greta» y

echo("CO2");
debería devolver «CO2». */

function echo() {
    return Greta;
}

function echo(C02) {
    return C02;
}

const x = echo('Greta');
const y = echo('C02');
console.log(x, y);
